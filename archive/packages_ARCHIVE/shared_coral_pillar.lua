--[[
	local grid = {}
	local pillars = 5
	for x = 1,pillars do
		for z = 1,pillars do
			local d2 = (2 / (pillars + 2)) / (pillars - 1)
			local d1 = (1 + d2) / pillars
			local g = {}
			g[#g + 1] = (x - 1) * d1 - 0.5
			g[#g + 1] = -0.5 
			g[#g + 1] = (z - 1) * d1 - 0.5
			g[#g + 1] = x * d1 - d2 - 0.5
			g[#g + 1] = 0.5 - (math.random(1,4) + math.random(4)) * 0.1 
			g[#g + 1] = z * d1 - d2 - 0.5
			grid[#grid + 1] = g
		end
	end
]]--
